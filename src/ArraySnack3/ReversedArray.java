package ArraySnack3;

import java.util.Arrays;

public class ReversedArray {
    public boolean arrayIsFull() {
        return true;
    }

    public static int arrayReversal(int[] arrayC) {
        for (int j = arrayC.length - 1; j > -1; j--) {
            System.out.print(" " + arrayC[j]);
        }
        return 0;
    }
}


